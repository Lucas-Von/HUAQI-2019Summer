package financial_management.util.PyInvoke.PyResponse;

public class PyResponseDemo {
    private int id;
    private String str;

    public PyResponseDemo() {
    }

    public PyResponseDemo(int id, String str) {
        this.id = id;
        this.str = str;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }
}
